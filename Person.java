package encapsulationPractice;

public class Person {
	
	private String name;
	private int age;
	
	public void setName(String myName) {
		name = myName;
	}
	
	public String getName() {
		return name;
	}

	public void setAge(int myAge) {
		age = myAge;
	}
	
	public int getAge() {
		return age;
	}

}
